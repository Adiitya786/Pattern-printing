public class Floyds_Triangle {
    public static void main(String[] args) {
        int rows =5;
        int a=1;
         for(int i =1;i<=rows;i++){
            for(int j=1;j<=i;j++){
                System.out.print(a);
                a++;
            }
            System.out.println("");
            
        }
    }
}
